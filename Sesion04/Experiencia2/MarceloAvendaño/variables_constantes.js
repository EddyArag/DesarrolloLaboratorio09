// a) USO DE VARIABLES Y CONSTANTES
{
    // Declarar una variable con ámbito de bloque
    let variableDeBloque = "Soy una variable de bloque";

    // Declarar una constante con ámbito de bloque
    const constanteDeBloque = "Soy una constante de bloque";

    console.log(variableDeBloque); // Acceso dentro del bloque
    console.log(constanteDeBloque); // Acceso dentro del bloque
}

